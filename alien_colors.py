alien_color = 'green'

if alien_color == 'green':
    print("Player earned 5 points for shooting a green alien.")

elif alien_color == 'yellow':
    print("Player earned 10 points for shooting a yellow alien.")

elif alien_color == 'red':
    print("Player earned 15 points for shooting a red alien.")

else:
    print("Player earned 0 points.")
